namespace Tizen.UIExtensions.Common
{
    /// <summary>
    /// Enumeration specifying vertical or horizontal scrolling directions.
    /// </summary>
    public enum ScrollOrientation
    {
        /// <summary>
        /// Scroll vertically.
        /// </summary>
        Vertical,
        /// <summary>
        /// Scroll Horizontally.
        /// </summary>
        Horizontal,
        /// <summary>
        /// Scroll both horizontally and vertically.
        /// </summary>
        Both,
        Neither
    }
}
